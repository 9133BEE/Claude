$s1 = $wb.Sheets.Item(1)
$s3 = $wb.Sheets.Item(3)
$lastRow = $s3.UsedRange.Rows.Count

# 改標頭
$s3.Cells.Item(1, 2).Value2 = "英文標籤"

$engFilled = 0
$chnFilled = 0
$splitDone = 0
$notFound  = 0

for ($r = 2; $r -le $lastRow; $r++) {
    $tag = $s3.Cells.Item($r, 2).Text
    if ([string]::IsNullOrWhiteSpace($tag)) { continue }

    $hasChinese = $tag -match "[一-鿿]"
    $hasParen   = $tag -match "[（(].+[)）]"

    if ($hasParen) {
        # 有括號 → 拆分
        if ($tag -match "^(.+?)[\s]*[（(](.+?)[)）][\s]*$") {
            $s3.Cells.Item($r, 2).Value2 = $matches[1].TrimEnd()
            $s3.Cells.Item($r, 3).Value2 = $matches[2]
            $splitDone++
        }
    } elseif (-not $hasChinese) {
        # 純英文 → 補中文
        if ($engToChn.ContainsKey($tag)) {
            $s3.Cells.Item($r, 3).Value2 = $engToChn[$tag]
            $engFilled++
        } else {
            $notFound++
            Write-Host "  [未翻譯-英] $tag"
        }
    } else {
        # 有中文 → 補英文（英文放欄2，中文移欄3）
        if ($chnToEng.ContainsKey($tag)) {
            $s3.Cells.Item($r, 2).Value2 = $chnToEng[$tag]
            $s3.Cells.Item($r, 3).Value2 = $tag
            $chnFilled++
        } else {
            $notFound++
